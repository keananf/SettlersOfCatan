package comm.messages;

public class ResourceCount
{
	private int wool, ore, grain, brick, lumber;

	/**
	 * @return the wool
	 */
	public int getWool()
	{
		return wool;
	}

	/**
	 * @param wool the wool to set
	 */
	public void setWool(int wool)
	{
		this.wool = wool;
	}

	/**
	 * @return the ore
	 */
	public int getOre()
	{
		return ore;
	}

	/**
	 * @param ore the ore to set
	 */
	public void setOre(int ore)
	{
		this.ore = ore;
	}

	/**
	 * @return the grain
	 */
	public int getGrain()
	{
		return grain;
	}

	/**
	 * @param grain the grain to set
	 */
	public void setGrain(int grain)
	{
		this.grain = grain;
	}

	/**
	 * @return the brick
	 */
	public int getBrick()
	{
		return brick;
	}

	/**
	 * @param brick the brick to set
	 */
	public void setBrick(int brick)
	{
		this.brick = brick;
	}

	/**
	 * @return the lumber
	 */
	public int getLumber()
	{
		return lumber;
	}

	/**
	 * @param lumber the lumber to set
	 */
	public void setLumber(int lumber)
	{
		this.lumber = lumber;
	}
}
